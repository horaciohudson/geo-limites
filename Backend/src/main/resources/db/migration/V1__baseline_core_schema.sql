CREATE OR REPLACE VIEW vw_properties_summary AS
SELECT
    p.property_id,
    p.registration_number,
    p.name,
    p.property_type,
    p.street,
    p.number,
    p.complement,
    p.neighborhood,
    p.city,
    p.state,
    p.zip_code,
    p.owner_name,
    p.owner_document,
    p.owner_email,
    p.owner_phone,
    p.total_area,
    p.total_perimeter,
    p.datum,
    p.coordinate_system,
    p.active,
    p.created_at,
    p.updated_at,
    p.user_id,
    1 AS total_owners,
    COALESCE(doc.document_count, 0) AS total_documents,
    0 AS total_files,
    0 AS total_dxf_files,
    ''::TEXT AS dxf_files_list,
    (
        p.street ||
        CASE WHEN p.number IS NOT NULL THEN ', ' || p.number ELSE '' END ||
        CASE WHEN p.complement IS NOT NULL THEN ', ' || p.complement ELSE '' END ||
        ' - ' || p.neighborhood ||
        ', ' || p.city || ' - ' || p.state ||
        CASE WHEN p.zip_code IS NOT NULL THEN ' - ' || p.zip_code ELSE '' END
    ) AS full_address,
    CASE
        WHEN p.registration_number IS NOT NULL
         AND p.street IS NOT NULL
         AND p.neighborhood IS NOT NULL
         AND p.city IS NOT NULL
         AND p.state IS NOT NULL
         AND p.owner_name IS NOT NULL
         AND p.owner_document IS NOT NULL THEN 'COMPLETO'
        ELSE 'INCOMPLETO'
    END AS completeness_status
FROM tab_properties p
LEFT JOIN (
    SELECT property_id, COUNT(*) AS document_count
    FROM tab_property_documents
    GROUP BY property_id
) doc ON doc.property_id = p.property_id
WHERE p.active = TRUE;
